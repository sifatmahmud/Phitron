WITH EMP_SAL AS(
SELECT AVG(SALARY) SAL
FROM EMPLOYEES
)
SELECT CONCAT(FIRST_NAME, ' ',  LAST_NAME) AS FULL_NAME
FROM EMPLOYEES
WHERE SALARY > (SELECT SAL FROM EMP_SAL);

