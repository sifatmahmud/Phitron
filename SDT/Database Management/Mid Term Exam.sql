SELECT DEPARTMENT_NAME, COUNT(EMPLOYEES.EMPLOYEE_ID) AS Number_of_Employees
FROM DEPARTMENTS
	LEFT JOIN EMPLOYEES ON EMPLOYEES.DEPARTMENT_ID = DEPARTMENTS.DEPARTMENT_ID
    GROUP BY DEPARTMENTS.DEPARTMENT_NAME;
